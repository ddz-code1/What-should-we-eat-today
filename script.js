// 加载本地存储数据
function loadData() {
    selectedIngredients = JSON.parse(localStorage.getItem(STORAGE_KEYS.SELECTED_INGREDIENTS)) || [];
    currentQuantity = parseInt(localStorage.getItem('currentQuantity')) || 7;

    // 更新UI
    document.getElementById('quantity').value = currentQuantity;

    // 恢复选中的食材
    selectedIngredients.forEach(ingredient => {
        const btn = document.querySelector(`.tag-btn[data-name="${ingredient}"]`);
        if (btn) btn.classList.add('selected');
    });
}

// 保存数据到本地存储
function saveData() {
    localStorage.setItem(STORAGE_KEYS.SELECTED_INGREDIENTS, JSON.stringify(selectedIngredients));
    localStorage.setItem('currentQuantity', currentQuantity);
}

