// 批量删除历史记录
function batchDeleteHistory() {
    const checkedCheckboxes = document.querySelectorAll('.history-check:checked');
    const checkedIndices = Array.from(checkedCheckboxes).map(checkbox => parseInt(checkbox.dataset.index));
    
    if (checkedIndices.length === 0) return;
    
    if (confirm(`确定要删除选中的 ${checkedIndices.length} 条历史记录吗？`)) {
        let history = JSON.parse(localStorage.getItem(STORAGE_KEYS.HISTORY)) || [];
        // 按索引从大到小排序，避免删除后索引错乱
        checkedIndices.sort((a, b) => b - a);
        checkedIndices.forEach(index => {
            history.splice(index, 1);
        });
        localStorage.setItem(STORAGE_KEYS.HISTORY, JSON.stringify(history));
        showToast(`已删除 ${checkedIndices.length} 条历史记录`, 'success');
        showHistory(); // 重新渲染列表
    }
}

