    selectAll.addEventListener('change', () => {
        checkboxes.forEach(checkbox => {
            checkbox.checked = selectAll.checked;
        });
        updateFavoriteBatchBtn();
    });

    // 单个选择
    checkboxes.forEach(checkbox => {
        checkbox.addEventListener('change', updateFavoriteBatchBtn);
    });

    // 批量删除
    batchDeleteBtn.addEventListener('click', batchDeleteFavorites);

    // 更新批量删除按钮状态
