    ingredients.sort((a, b) => {
        const aExpiry = new Date(a.purchaseDate);
        aExpiry.setDate(aExpiry.getDate() + a.shelfLife);
        const bExpiry = new Date(b.purchaseDate);
        bExpiry.setDate(bExpiry.getDate() + b.shelfLife);
        return aExpiry - bExpiry;
    });

