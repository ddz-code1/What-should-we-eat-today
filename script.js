// 生成随机菜谱
function generateRandomRecipes() {
    showLoading();

    setTimeout(() => {
        hideLoading();

        const list = document.getElementById('random-recipes');
        list.innerHTML = '';

        // 获取所有菜谱（系统内置 + 自定义）
        const allRecipes = getAllRecipes();

        // 随机选择菜谱
        const shuffled = [...allRecipes].sort(() => 0.5 - Math.random());
        const selected = shuffled.slice(0, currentQuantity);

        selected.forEach(recipe => {
            const card = document.createElement('div');
            card.className = 'recipe-card';
            const recipeType = recipe.id.toString().length > 10 ? 'custom' : 'system';
            card.innerHTML = `
                <div class="recipe-header">
                    <div class="recipe-name">${recipe.name}</div>
                </div>
                <div class="recipe-info">
                    <span>⏱️ ${recipe.time}</span>
                    <span>📊 ${recipe.difficulty}</span>
                </div>
            `;

            // 绑定整个卡片的点击事件
            card.addEventListener('click', () => {
                if (recipeType === 'custom') {
                    showCustomRecipeDetail(recipe.id);
                } else {
                    showRecipeDetail(parseInt(recipe.id));
                }
            });
            
            list.appendChild(card);
        });
    }, 1000);
}

