// 添加食材
function addIngredient(e) {
    e.preventDefault();

    const formData = new FormData(e.target);
    const ingredients = JSON.parse(localStorage.getItem(STORAGE_KEYS.INGREDIENTS)) || [];

    if (editingIngredientId) {
        // 更新
        const idx = ingredients.findIndex(i => i.id === editingIngredientId);
        if (idx > -1) {
            ingredients[idx] = {
                ...ingredients[idx],
                name: formData.get('ingredient-name'),
                purchaseDate: formData.get('purchase-date'),
                shelfLife: parseInt(formData.get('shelf-life')),
                notes: formData.get('notes') || ''
            };
        }
    } else {
        // 新增
        const ingredient = {
            id: Date.now().toString(),
            name: formData.get('ingredient-name'),
            purchaseDate: formData.get('purchase-date'),
            shelfLife: parseInt(formData.get('shelf-life')),
            notes: formData.get('notes') || ''
        };
        ingredients.push(ingredient);
    }

    localStorage.setItem(STORAGE_KEYS.INGREDIENTS, JSON.stringify(ingredients));

    document.getElementById('add-ingredient-modal').classList.remove('show');
    showToast(editingIngredientId ? '食材已更新！' : '食材添加成功！', 'success');
    editingIngredientId = null;
    showExpirationManager();
    updateExpirationBadge();
}

