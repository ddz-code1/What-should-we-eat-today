// 添加或编辑自定义菜谱
function addCustomRecipe(e) {
    e.preventDefault();

    const formData = new FormData(e.target);
    const customRecipes = JSON.parse(localStorage.getItem(STORAGE_KEYS.CUSTOM_RECIPES)) || [];

    const recipeData = {
        id: editingCustomId || Date.now().toString(),
        name: formData.get('recipe-name'),
        ingredients: formData.get('recipe-ingredients').split(/[,，]/).map(item => item.trim()).filter(item => item), // 支持英文逗号和中文逗号，过滤空项
        steps: formData.get('recipe-steps').split('\n').filter(step => step.trim() !== ''),
        time: formData.get('recipe-time'),
        difficulty: formData.get('recipe-difficulty'),
        type: formData.get('recipe-type')
    };

    if (editingCustomId) {
        // 编辑已有菜谱
        const index = customRecipes.findIndex(recipe => recipe.id === editingCustomId);
        if (index > -1) {
            customRecipes[index] = recipeData;
        }
    } else {
        // 添加新菜谱
        customRecipes.push(recipeData);
    }

    localStorage.setItem(STORAGE_KEYS.CUSTOM_RECIPES, JSON.stringify(customRecipes));
    document.getElementById('add-custom-recipe-modal').classList.remove('show');
    showToast(editingCustomId ? '菜谱已更新！' : '菜谱添加成功！', 'success');
    editingCustomId = null;
    showCustomRecipes();
}

