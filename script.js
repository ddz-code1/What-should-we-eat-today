// 食材选择切换
function toggleIngredient(e) {
    const btn = e.target;
    const ingredient = btn.dataset.name;

    if (btn.classList.contains('selected')) {
        // 取消选择
        btn.classList.remove('selected');
        selectedIngredients = selectedIngredients.filter(item => item !== ingredient);
    } else {
        // 选择
        if (selectedIngredients.length >= 10) {
            showToast('最多只能选择10种食材哦～', 'warning');
            return;
        }
        btn.classList.add('selected');
        selectedIngredients.push(ingredient);
    }

    saveData();
    updateSelectedIngredientsBar();
    generateRecipes();
}



// 模式切换
function changeMode(e) {
    const btn = e.target;
    const mode = btn.dataset.mode;

    // 移除其他模式的选中状态
    document.querySelectorAll('.mode-btn').forEach(b => b.classList.remove('active'));

    // 选中当前模式
    btn.classList.add('active');
    currentMode = mode;

    generateRecipes();
}

// 更新已选食材悬浮栏
function updateSelectedIngredientsBar() {
    const bar = document.getElementById('selected-ingredients');
    const list = bar.querySelector('.selected-list');

    if (selectedIngredients.length === 0) {
        bar.classList.remove('show');
        return;
    }

    bar.classList.add('show');
    list.innerHTML = '';

    selectedIngredients.forEach(ingredient => {
        const item = document.createElement('div');
        item.className = 'selected-item';
        item.innerHTML = `
            <span>${ingredient}</span>
            <button class="remove-btn" data-name="${ingredient}">×</button>
        `;
        list.appendChild(item);

        // 绑定删除事件
        item.querySelector('.remove-btn').addEventListener('click', removeIngredient);
    });

    // 如果超过5个，显示+号
    if (selectedIngredients.length > 5) {
        const moreItem = document.createElement('div');
        moreItem.className = 'selected-item';
        moreItem.innerHTML = `<span>+${selectedIngredients.length - 5}</span>`;
        list.appendChild(moreItem);
    }
}

// 删除单个食材
function removeIngredient(e) {
    const ingredient = e.target.dataset.name;
    selectedIngredients = selectedIngredients.filter(item => item !== ingredient);

    // 更新UI
    document.querySelector(`.tag-btn[data-name="${ingredient}"]`).classList.remove('selected');

    saveData();
    updateSelectedIngredientsBar();
    generateRecipes();
}

// 清空所有食材
function clearIngredients() {
    selectedIngredients.forEach(ingredient => {
        const btn = document.querySelector(`.tag-btn[data-name="${ingredient}"]`);
        if (btn) btn.classList.remove('selected');
    });
    selectedIngredients = [];
    saveData();
    updateSelectedIngredientsBar();
    generateRecipes();
    showToast('已清空选中食材', 'success');
}

// 生成菜谱
function generateRecipes() {
    const list = document.getElementById('recipe-list');
    list.innerHTML = '';

    if (selectedIngredients.length === 0) {
        list.innerHTML = '<p style="text-align: center; color: #7f8c8d; padding: 40px;">请至少选择1种食材哦～</p>';
        return;
    }

    // 获取所有菜谱（系统内置 + 自定义）
    const allRecipes = getAllRecipes();

    let filteredRecipes;
    if (currentMode === 'fuzzy') {
        // 模糊匹配：只要包含至少一种选中的食材
        filteredRecipes = allRecipes.filter(recipe =>
            selectedIngredients.some(ing => recipe.ingredients.includes(ing))
        );
    } else {
        // 精确匹配：必须包含所有选中的食材
        filteredRecipes = allRecipes.filter(recipe =>
            selectedIngredients.every(ing => recipe.ingredients.includes(ing))
        );
    }

    // 按匹配度排序（包含食材数量多的优先）
    filteredRecipes.sort((a, b) => {
        const aMatch = a.ingredients.filter(ing => selectedIngredients.includes(ing)).length;
        const bMatch = b.ingredients.filter(ing => selectedIngredients.includes(ing)).length;
        return bMatch - aMatch;
    });

    if (filteredRecipes.length === 0) {
        list.innerHTML = '<p style="text-align: center; color: #7f8c8d; padding: 40px;">没有找到匹配的菜谱，试试其他食材组合吧～</p>';
        return;
    }

    filteredRecipes.forEach(recipe => {
            const card = document.createElement('div');
            card.className = 'recipe-card';
            card.innerHTML = `
                <div class="recipe-header">
                    <div class="recipe-name">${recipe.name}</div>
                </div>
                    <div class="recipe-info">
                        <span>⏱️ ${recipe.time}</span>
                        <span>📊 ${recipe.difficulty}</span>
                    </div>
            `;

            // 绑定整个卡片的点击事件
            card.addEventListener('click', () => showRecipeDetail(recipe.id));

            list.appendChild(card);
        });

    // 保存到历史记录
    saveToHistory(filteredRecipes.slice(0, 5));
}

// 显示菜谱详情
function showRecipeDetail(recipeId) {
    const allRecipes = getAllRecipes();
    const recipe = allRecipes.find(r => r.id === recipeId);
    if (!recipe) return;

    const modal = document.getElementById('recipe-modal');
    const title = document.getElementById('recipe-title');
    const details = document.getElementById('recipe-details');

    title.textContent = recipe.name;

    details.innerHTML = `
        <div class="recipe-meta">
            <span>⏱️ ${recipe.time}</span>
            <span>📊 ${recipe.difficulty}</span>
            <span>🍽️ ${recipe.type}</span>
        </div>
        <div class="recipe-ingredients">
            <h4>食材清单</h4>
            <ul>
                ${recipe.ingredients.map(ing => `<li>${ing}</li>`).join('')}
            </ul>
        </div>
        <div class="recipe-steps">
            <h4>制作步骤</h4>
            <ol>
                ${recipe.steps.map(step => `<li>${step}</li>`).join('')}
            </ol>
        </div>
    `;

    // 检查是否已收藏
    const favorites = JSON.parse(localStorage.getItem(STORAGE_KEYS.FAVORITES)) || [];
    const isFavorited = favorites.includes(recipeId);
    document.getElementById('favorite-recipe').innerHTML = isFavorited ?
        '<i class="fas fa-heart"></i> 已收藏' :
        '<i class="fas fa-heart"></i> 收藏';

    modal.classList.add('show');
}

