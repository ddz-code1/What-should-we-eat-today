// 更新过期提醒徽章
function updateExpirationBadge() {
    const ingredients = JSON.parse(localStorage.getItem(STORAGE_KEYS.INGREDIENTS)) || [];
    const now = new Date();
    let urgentCount = 0;

    ingredients.forEach(ing => {
        const purchaseDate = new Date(ing.purchaseDate);
        const expiryDate = new Date(purchaseDate);
        expiryDate.setDate(expiryDate.getDate() + ing.shelfLife);
        const daysLeft = Math.ceil((expiryDate - now) / (1000 * 60 * 60 * 24));

        if (daysLeft <= 3) {
            urgentCount++;
        }
    });

    const badge = document.getElementById('expiration-badge-nav');
    if (badge) {
        if (urgentCount > 0) {
            badge.textContent = urgentCount;
            badge.classList.add('show');
        } else {
            badge.classList.remove('show');
        }
    }
}

// 检查过期提醒
function checkExpirationReminders() {
    const ingredients = JSON.parse(localStorage.getItem(STORAGE_KEYS.INGREDIENTS)) || [];
    const now = new Date();
    const lastCheck = localStorage.getItem(STORAGE_KEYS.LAST_EXPIRATION_CHECK);
    const lastCheckTime = lastCheck ? new Date(lastCheck) : new Date(0);

    let expiredItems = [];
    let warningItems = [];

    ingredients.forEach(ing => {
        const purchaseDate = new Date(ing.purchaseDate);
        const expiryDate = new Date(purchaseDate);
        expiryDate.setDate(expiryDate.getDate() + ing.shelfLife);
        const daysLeft = Math.ceil((expiryDate - now) / (1000 * 60 * 60 * 24));

        if (daysLeft <= 0) {
            expiredItems.push(ing);
        } else if (daysLeft <= 3) {
            warningItems.push(ing);
        }
    });

    // 过期提醒（每次打开都显示）
    if (expiredItems.length > 0) {
        showExpirationModal(expiredItems);
    }
    // 即将过期提醒（首次打开或距离上次检查超过4小时）
    else if (warningItems.length > 0 && (now - lastCheckTime) > 4 * 60 * 60 * 1000) {
        const hoursSinceLastCheck = (now - lastCheckTime) / (1000 * 60 * 60);
        if (hoursSinceLastCheck > 4) {
            showExpirationToast(warningItems);
            localStorage.setItem(STORAGE_KEYS.LAST_EXPIRATION_CHECK, now.toISOString());
        }
    }

    updateExpirationBadge();
}

// 显示过期弹窗
function showExpirationModal(expiredItems) {
    const modal = document.getElementById('expiration-modal');
    const list = document.getElementById('expiration-list');

    // 格式化日期函数
    const formatDate = (dateString) => {
        const date = new Date(dateString);
        return `${date.getFullYear()}-${(date.getMonth() + 1).toString().padStart(2, '0')}-${date.getDate().toString().padStart(2, '0')}`;
    };

    // 计算剩余天数
    const getDaysLeft = (purchaseDate, shelfLife) => {
        const now = new Date();
        const expiryDate = new Date(purchaseDate);
        expiryDate.setDate(expiryDate.getDate() + shelfLife);
        return Math.ceil((expiryDate - now) / (1000 * 60 * 60 * 24));
    };

    list.innerHTML = `
        <div class="expiration-header">
            <h4>🥲 已过期食材（${expiredItems.length}种）</h4>
            <p class="expiration-subtitle">建议及时处理，避免浪费</p>
        </div>
        <div class="expiration-items">
            ${expiredItems.length > 0 ? expiredItems.map(item => {
                const daysLeft = getDaysLeft(item.purchaseDate, item.shelfLife);
                const purchaseDate = formatDate(item.purchaseDate);
                const expiryDate = formatDate(new Date(new Date(item.purchaseDate).setDate(new Date(item.purchaseDate).getDate() + item.shelfLife)));
                return `
                    <div class="expiration-item expired" data-id="${item.id}">
                        <div class="expiration-item-info">
                            <div class="expiration-item-name">${item.name}</div>
                            <div class="expiration-item-meta">
                                <span class="meta-item">购买日期：${purchaseDate}</span>
                                <span class="meta-item">过期日期：${expiryDate}</span>
                                <span class="meta-item expired-days">已过期 ${Math.abs(daysLeft)} 天</span>
                            </div>
                            ${item.notes ? `<div class="expiration-item-notes">备注：${item.notes}</div>` : ''}
                        </div>
                        <button class="expiration-item-delete" onclick="deleteExpiredIngredient('${item.id}')">
                            <i class="fas fa-trash"></i>
                        </button>
                    </div>
                `;
            }).join('') : '<p class="no-items">没有已过期的食材</p>'}
        </div>
    `;

    modal.classList.add('show');
}

